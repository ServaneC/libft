#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	cmp;

	i = 0;
	cmp = c;
	while (((s[i] + 1 && cmp == '\0') || s[i]) && s != NULL)
	{
		if (s[i] == cmp)
			return ((char *)&s[i]);
		i++;
	}
	return (0);
}
