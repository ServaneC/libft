#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		len;

	len = ft_strlen((char *)s);
	while (len && s[len] != (char)c && s != NULL)
		len--;
	if (s[len] == (char)c && s != NULL)
		return ((char *)&s[len]);
	return (NULL);
}
